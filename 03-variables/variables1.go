package main

import "fmt"

func main() {
	var a1 string
	a1 = "variable definition 1"
	fmt.Println(a1)

	var a2 string = "variable definition 2"
	fmt.Println(a2)

	a3 := "variable definition 3"
	fmt.Println(a3)

	var a4 int
	a4 = 5
	fmt.Println(a4)

	var a5 int = 10
	fmt.Println(a5)

	a6 := 15
	fmt.Println(a6)
}
